    <h1>Box-andning</h1>
    <p>Box-andning, även kallad fyrkants-andning, har varit förknippad med allt från förbättrad hjärthälsa, minskad ångest och förbättrad kognitiv funktion. Tekniken är enkel och innebär att man andas in genom näsan i 4 sekunder, håller andan i 4 sekunder, andas ut genom munnen i 4 sekunder och håller andan i 4 sekunder. Man upprepar detta i flera cykler.</p>
